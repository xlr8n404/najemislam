export function getTopTags(): string[] {
  return TOP_TAGS;
}

export function searchTags(query: string): string[] {
  if (!query.trim()) {
    return getTopTags();
  }

  const lowerQuery = query.toLowerCase();
  return IDENTITY_TAGS.filter(tag =>
    tag.toLowerCase().includes(lowerQuery)
  );
}
